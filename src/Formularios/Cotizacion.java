    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        float Total = Float.valueOf(this.txtTotal.getText());
        
    DefaultTableModel modelo = (DefaultTableModel) this.grdDetalle.getModel();
    modelo.addRow(new Object[]{this.txtCodigo.getText(),this.txtNombreProducto.getText(),this.txtPresentacion.getText(),this.txtPrecio.getText(),this.txtCantidad.getText(),this.txtTotal.getText()});
    this.txtCodigo.setText("");
    this.txtNombreProducto.setText("");
    this.txtPresentacion.setText("");
    this.txtPrecio.setText("");
    this.txtCantidad.setText("");
    this.txtTotal.setText("");
    this.CalcularTotales();
    }//GEN-LAST:event_btnAgregarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
          if(this.grdDetalle.getSelectedRow()>-1)
        {
         DefaultTableModel modelo = (DefaultTableModel) this.grdDetalle.getModel();
         int registro = this.grdDetalle.getSelectedRow();
         modelo.removeRow(registro);
            
        }
          this.CalcularTotales();
    }//GEN-LAST:event_btnEliminarMouseClicked

    private void txtCantidadFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCantidadFocusLost
        // TODO add your handling code here:
        float Precio =  Float.parseFloat(this.txtPrecio.getText());
        float Cantidad =  Float.parseFloat(this.txtCantidad.getText());
        float Total = Precio*Cantidad;
        this.txtTotal.setText(Float.toString(Total));
    }//GEN-LAST:event_txtCantidadFocusLost

