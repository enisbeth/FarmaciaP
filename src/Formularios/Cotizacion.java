    private void CalcularTotales()
    {   
        float SubTotal = 0;
        
        for(int i =0 ;i<=this.grdDetalle.getRowCount()-1;i++)
        {
         DefaultTableModel modelo = (DefaultTableModel) this.grdDetalle.getModel();
         SubTotal = SubTotal + Float.parseFloat(modelo.getValueAt(i, 5).toString());
        }
        
        
        float Impuesto = SubTotal*15/100;
        float GranTotal = SubTotal + Impuesto;
       
        this.txtSubTotal.setText(Float.toString(SubTotal));
        this.txtImpuesto.setText(Float.toString(Impuesto));
      this.txtGranTotal.setText(Float.toString(GranTotal));
    }
    
